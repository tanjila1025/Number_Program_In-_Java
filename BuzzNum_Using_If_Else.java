class BuzzNum_Using_If_Else{
    
    public static void main(String[] args){
    
     int num=49;

     if(num % 10 == 7 || num % 7 == 0)
     {
        System.out.println("Number is Buzz Number!");
     }
       else
       {
         System.out.println("Number is not Buzz Number");
        }
   }
}/*OUTPUT:
   Number is Buzz Number!
*/